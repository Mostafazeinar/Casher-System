    private void jButton136ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton136ActionPerformed
        // TODO add your handling code here:

        try {
            String current_item_id = items_table.getValueAt(currentRow, 0).toString();

            for (int i = 0; i < soldItems.size(); i++) {
                if (soldItems.get(i).getName().equals(current_item_id)) {
                    price_txt.setText(Double.parseDouble(price_txt.getText()) - soldItems.get(i).getTotal() + "");
                    soldItems.remove(i);
                }
            }
            tableModel.removeRow(currentRow);

        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton136ActionPerformed

    private void jButton137ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton137ActionPerformed
        // TODO add your handling code here:

        try {
            Invoice in = new Invoice();
            in.setId(Integer.parseInt(Data.AutoIncrementCoulmn(Data.con, "invoice", "id")));
            in.setDiscount(Double.parseDouble(disc_txt.getText()));
            in.setPad(Double.parseDouble(taf_txt.getText()));
            in.setPbd(Double.parseDouble(price_txt.getText()));
            in.add(Data.con);

            for (int i = 0; i < soldItems.size(); i++) {
                Sold_items s = new Sold_items();
                s.setId(Integer.parseInt(Data.AutoIncrementCoulmn(Data.con, "sold_items", "id")));
                s.setInvoice_id(in.getId());
                s.setItem_id(Integer.parseInt(Data.getCoulmnData(Data.con, "item", "item_id", " where name = '" + soldItems.get(i).getName() + "';")[0]));
                s.setQuantity(soldItems.get(i).getQuantity());
                s.setSize_id(soldItems.get(i).getSize_id());
                s.add(Data.con);
            }
            totalWholeProject += Double.parseDouble(taf_txt.getText());

            if (startingId > in.getId()) {
                startingId = in.getId();

            }

            if (startingId == 0) {
                startingId = Integer.parseInt(Data.AutoIncrementCoulmn(Data.con, "invoice", "id"));

            }

            Helper.print(soldItems, in.getId() + "", price_txt.getText(), disc_txt.getText(), taf_txt.getText());
            Thread.sleep(7000);
            Helper.print(soldItems, in.getId() + "", price_txt.getText(), disc_txt.getText(), taf_txt.getText());
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton137ActionPerformed

