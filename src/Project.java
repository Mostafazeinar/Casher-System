    private void jButton53ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton53ActionPerformed
        // TODO add your handling code here:
        try {
            BillData b = new BillData();
            b.setName(Data.getCoulmnData(Data.con, "item", "name", " where item_id = " + item_id)[0]);
            b.setPrice(Double.parseDouble(Data.getCoulmnData(Data.con, "price", "cost", " where size_id = " + size_id + " and item_id = " + item_id)[0]));
            try {
                b.setQuantity(Integer.parseInt(quntitytxt.getText()));
            } catch (Exception e) {
                b.setQuantity(0);
            }
            // b.setTotal(b.getQuantity() * b.getPrice());
            b.setTotal(b.getQuantity() * b.getPrice());
            b.setSize_id(size_id);
            if (size_id == 1) {
                b.setSize_name("L");
            } else if (size_id == 2) {
                b.setSize_name("M");
            }
            //total = Double.parseDouble(b.setTotal(b.getQuantity() * b.getPrice()));
//            items_table.setModel(tableModel);
            tableModel.addRow(new Object[]{b.getName(), b.getPrice(), b.getQuantity(), b.getTotal()});
            soldItems.add(b);

            price_txt.setText(Double.parseDouble(price_txt.getText()) + b.getTotal() + "");

        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton53ActionPerformed

